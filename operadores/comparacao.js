//Igual (==)
//Retorna verdadeiro caso os operandos sejam iguais. 3 == var1
var var1 = "3"; 
console.log(3 == var1);

//Diferente (!=)
//Retorna verdadeiro caso os operandos sejam diferentes. 3 == var1
console.log(3 != var1);

//Estritamente igual (===)
//Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo. 3 === 3
console.log(3 === var1);

//Estritamente diferente (!==)
//Retorna verdadeiro caso os operandos sejam diferentes e/ou do tipos diferntes. 3 !== "3"
console.log(3 !== var1);

//Maior que (>)
//Retorna verdadeiro caso o primeiro operador seja maior que o segundo. 4 > 3
console.log(4>3);

//menor que (>)
//Retorna verdadeiro caso o primeiro operador seja menor que o segundo. 4 < 5
console.log(4<3);

//Maior ou igual que (>=)
//Retorna verdadeiro caso o primeiro operador seja maior ou igual ao segundo. 4 >= 3
console.log(4>=4);

//Menor ou igual que (<=)
//Retorna verdadeiro caso o primeiro operador seja menor ou igual ao segundo. 4 <= 5
console.log(4<=4);
