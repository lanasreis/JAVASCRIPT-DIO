/*O QUE É?
1 - Soluções generalistas para problemas recorrentes durante o desenvolvimento de um software.
2 - NÃO são frameworks ou um código pronto.

FORMATO
1 - Nome
2 - Exemplo
3 - Contexto
4 - Problema
5 - Solução 

CATEGORIAS
 - Criação -> Asbtraem e/ou adiam o processo de criaçãodos objetos. Tornam o sistema independente de como seus objetos são criados, compostos e reperesentados.
    ¬ Abstract Factory
    ¬ Builder
    ¬ Factory Method
    ¬ Prototype
    ¬ Singleton

 - Estruturais -> Preocupam com a forma como classes e objetos são compostos para formar estruturas maiores.
    ¬ Adapter
    ¬ Bridge
    ¬ Composite
    ¬ Decorator
    ¬ Facade
    ¬ Business Delegate
    ¬ Flyweight
    ¬ Proxy

 - Comportamentais -> Algoritmos e atribuições entre os objetos. Não desecrevem apenas padrões de objetos ou de classes, mas também o padrão de comunicação entre os objetos.
    ¬ Chain of Responsibility
    ¬ Command
    ¬ Interpreter
    ¬ Iterator
    ¬ Mediator
    ¬ Observe
    ¬ State
    ¬ Strategy
    ¬ Template Method
    ¬ Visitor 
    
    
MAIS UTILIZADOS
1 - Factory
2 - Singleton
3 - Decorator
4 - Observe
5 - Module 
*/